import math
r1=float(input("Radius of The First Circle : "))
r2=float(input("Radius of The Second Circle : "))
print(f"Area of The First Circle : {math.pi*r1*r1:.2f}")
print(f"Area of The Second Circle : {math.pi*r2*r2:.2f}")
print(f"Perimeter of The First Circle : {math.pi*2*r1:.2f}")
print(f"Perimeter of The Second Circle : {math.pi*2*r2:.2f}")
